    public Vector2 cellSize = new Vector2(1, 1);
    public Vector3 wallScale = new Vector3(1, 1, 1); // Escala para las paredes

    void Awake()
    {
        GenerateMaze(); // Genera el laberinto al cargar la escena
    }

#if UNITY_EDITOR
    void Update()
    {
        if (!Application.isPlaying) // Permite regenerar en el editor sin ejecutar la escena
        {
            GenerateMaze();
        }
    }
#endif

    void GenerateMaze()
    {
        DestroyPreviousMaze(); // Asegura que no haya duplicados
        CombineMeshesForMaze(); // Combina las mallas del laberinto
    }

    void DestroyPreviousMaze()
    {
        GameObject walls = GameObject.Find("CombinedWalls");
        GameObject floors = GameObject.Find("CombinedFloors");

        if (walls) DestroyImmediate(walls);
        if (floors) DestroyImmediate(floors);
    }

    void CombineMeshesForMaze()
    {
        CombineInstance[] wallCombines = new CombineInstance[CountCells(1)];
        CombineInstance[] floorCombines = new CombineInstance[CountCells(0)];

        int wallIndex = 0;
        int floorIndex = 0;

        for (int x = 0; x < maze.GetLength(0); x++)
        {
            for (int y = 0; y < maze.GetLength(1); y++)
            {
                Vector3 position = new Vector3(x * cellSize.x, 0, y * cellSize.y);

                if (maze[x, y] == 1) // Pared
                {
                    wallCombines[wallIndex] = CreateCombineInstance(wallPrefab, position, wallScale);
                    wallIndex++;
                }
                else if (maze[x, y] == 0) // Suelo
                {
                    floorCombines[floorIndex] = CreateCombineInstance(floorPrefab, position, Vector3.one);
                    floorIndex++;
                }
            }
        }

        // Crear malla combinada para paredes
        if (wallCombines.Length > 0)
        {
            CreateCombinedObject(wallCombines, "CombinedWalls", wallPrefab.GetComponent<MeshRenderer>().sharedMaterial);
        }

        // Crear malla combinada para el suelo
        if (floorCombines.Length > 0)
        {
            CreateCombinedObject(floorCombines, "CombinedFloors", floorPrefab.GetComponent<MeshRenderer>().sharedMaterial);
        }
    }

    CombineInstance CreateCombineInstance(GameObject prefab, Vector3 position, Vector3 scale)
    {
        MeshFilter meshFilter = prefab.GetComponent<MeshFilter>();
        Matrix4x4 matrix = Matrix4x4.TRS(position, Quaternion.identity, scale);

        return new CombineInstance
        {
            mesh = meshFilter.sharedMesh,
            transform = matrix
        };
    }

    void CreateCombinedObject(CombineInstance[] combines, string name, Material material)
    {
        if (combines.Length == 0) return;

        GameObject combinedObject = new GameObject(name);

        MeshFilter meshFilter = combinedObject.AddComponent<MeshFilter>();
        MeshRenderer meshRenderer = combinedObject.AddComponent<MeshRenderer>();
        MeshCollider meshCollider = combinedObject.AddComponent<MeshCollider>();

        Mesh combinedMesh = new Mesh
        {
            indexFormat = UnityEngine.Rendering.IndexFormat.UInt32 // Permite más vértices
        };

        combinedMesh.CombineMeshes(combines);
        meshFilter.mesh = combinedMesh;

        meshRenderer.material = material;
        meshCollider.sharedMesh = combinedMesh;

        combinedObject.isStatic = true;
    }

    int CountCells(int value)
    {
        int count = 0;
        for (int x = 0; x < maze.GetLength(0); x++)
        {
            for (int y = 0; y < maze.GetLength(1); y++)
            {
                if (maze[x, y] == value) count++;
            }
        }
        return count;
    }
